package com.pluralsight.stringapplication;

public class VariableApp {
    public static void main(String[] args) {
        String color = ("red");
        System.out.println("The color is:" + color);
        int yearClass = 2025;
        System.out.println("The class year:" + yearClass);
        char middleName = 'a';
        System.out.println("My middle initial:" + middleName);
        boolean pets = true;
        System.out.println("I have pets:" + pets);
        String niceMessage = "Make mistakes and learn from them";
        System.out.println("Leave a nice message:" + niceMessage);
        int weekDays = 7;
        System.out.println("Number of week days:" + weekDays);
        float priceCoffee = 4.99f;
        System.out.println("Price on coffee:" + priceCoffee);
        char favoriteLetter = 'v';
        System.out.println("My favorite letter:" + favoriteLetter);
        boolean rainWeather = false;
        System.out.println("Weather outside:" + rainWeather);
    }
}